# Proposed EDM sampler (Algorithm 2).

def edm_sampler(
    net, latents, dataloader, class_labels=None, randn_like=torch.randn_like,
    num_steps=18, sigma_min=0.002, sigma_max=80, rho=7,
    S_churn=0, S_min=0, S_max=float('inf'), S_noise=1, optimal_denoiser=False
):
    # Adjust noise levels based on what's supported by the network.
    if optimal_denoiser:
        optimal_x0 = optimal_solver(dataloader)
    sigma_min = max(sigma_min, net.sigma_min)
    sigma_max = min(sigma_max, net.sigma_max)

    # Time step discretization.
    step_indices = torch.arange(num_steps, dtype=torch.float64, device=latents.device)
    t_steps = (sigma_max ** (1 / rho) + step_indices / (num_steps - 1) * (sigma_min ** (1 / rho) - sigma_max ** (1 / rho))) ** rho
    t_steps = torch.cat([net.round_sigma(t_steps), torch.zeros_like(t_steps[:1])]) # t_N = 0

    # Main sampling loop.
    x_next = latents.to(torch.float64) * t_steps[0]
    for i, (t_cur, t_next) in enumerate(zip(t_steps[:-1], t_steps[1:])): # 0, ..., N-1
        x_cur = x_next

        # Increase noise temporarily.
        gamma = min(S_churn / num_steps, np.sqrt(2) - 1) if S_min <= t_cur <= S_max else 0
        t_hat = net.round_sigma(t_cur + gamma * t_cur)
        x_hat = x_cur + (t_hat ** 2 - t_cur ** 2).sqrt() * S_noise * randn_like(x_cur)

        # Euler step.
        if not optimal_denoiser:
            denoised = net(x_hat, t_hat, class_labels).to(torch.float64)
        else:
            denoised = optimal_x0(x_hat, s = torch.tensor(1, device = t_cur.device, dtype = t_cur.dtype), sigma = t_hat)
        d_cur = (x_hat - denoised) / t_hat
        x_next = x_hat + (t_next - t_hat) * d_cur

        # Apply 2nd order correction.
        if i < num_steps - 1 and not optimal_denoiser:
            denoised = net(x_next, t_next, class_labels).to(torch.float64)
            d_prime = (x_next - denoised) / t_next
            x_next = x_hat + (t_next - t_hat) * (0.5 * d_cur + 0.5 * d_prime)

    return x_next


def normal_distribution_batch(x, y_batch, s, std, bias = 0):
    bs_y = y_batch.shape[0]
    bs_x = x.shape[0]
    xb = x.unsqueeze(1)
    prob = torch.exp(-(((xb - s * y_batch)**2).view(bs_x, bs_y, -1).sum(dim=-1).to(torch.float64)/std**2)/2 - bias.unsqueeze(1))
    # prob = torch.where(prob==torch.inf, 1, 0)
    prob_y = prob.clone().view(bs_x, bs_y, 1, 1, 1) * y_batch   # (bs_x, bs_y, 3, 32, 32)
    return prob.sum(dim=1, keepdim=True).squeeze(), prob_y.sum(dim=1, keepdim=True).squeeze() # (bs_x, ), (bs_x, 3, 32, 32)

def get_exp_bias_batch(x, y_batch, s, std):
    ## because exp() might return a very small number, we need a bias
    bs_y = y_batch.shape[0]
    bs_x = x.shape[0]
    xb = x.unsqueeze(1)
    return (-(((xb - s * y_batch)**2).view(bs_x, bs_y, -1).sum(dim=-1).to(torch.float64)/std**2)/2).max(dim=1)

def optimal_solver(dataloader):
    def optimal_sol(batch, s, sigma):
        
        std = s * sigma
        x = batch
        prob_sum = 0. * torch.ones(x.shape[0]).cuda()
        prob_y_sum = torch.zeros_like(x).to(torch.float64).cuda()
        exp_bias = -(torch.inf) * torch.ones(x.shape[0],).cuda()
        for y_batch, _ in dataloader:
            y_batch = y_batch.cuda().to(torch.float32) / 127.5 - 1
            curr_exp_bias = get_exp_bias_batch(x, y_batch, s, std)[0]
            exp_bias = torch.where(curr_exp_bias>exp_bias, curr_exp_bias, exp_bias)
        for y_batch, _ in dataloader:
            y_batch = y_batch.cuda().to(torch.float32) / 127.5 - 1
            prob, prob_y = normal_distribution_batch(x, y_batch, s, std, exp_bias)
            prob_sum += prob
            prob_y_sum += prob_y

        optimal_solution = prob_y_sum/prob_sum.view(-1, 1, 1, 1)  
        return optimal_solution
    return optimal_sol        

